package com.dxc.activitidemo.performanceprocess.entity;

public class EmployeeGoalEval {

    private Integer id;

    private String name;

    private String Group;

    private String title;

    private String descri;

    private String doData;

    private String status;

    private String mrEval;

    private String grade;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGroup() {
        return Group;
    }

    public void setGroup(String group) {
        Group = group;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescri() {
        return descri;
    }

    public void setDescri(String descri) {
        this.descri = descri;
    }

    public String getDoData() {
        return doData;
    }

    public void setDoData(String doData) {
        this.doData = doData;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getMrEval() {
        return mrEval;
    }

    public void setMrEval(String mrEval) {
        this.mrEval = mrEval;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }
}
